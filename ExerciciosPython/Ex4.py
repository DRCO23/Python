##Metros para centímetros e milímetros
metros = float(input("Insira o comprimento em metros (utilize '.' para decimal): "))
centimetros = (metros*100)
milimetros = (metros*1000)
print("Valor em Metros:",metros, "m")
print("Valor em Centímetros:",centimetros, "cm")
print("Valor em Milímetros:",milimetros, "mm")